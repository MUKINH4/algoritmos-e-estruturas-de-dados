public class Ex03 {
    
    public static void main(String[] args) {
        // Crie um programa que imprima os números de 1 a 10 usando um loop.

        // While
        int i = 1;
        while (i <= 10) {
            System.out.println("(While) Indíce: " + i);
            i++;
        }

        // For
        for (int j = 1; j <= 10; j++) {
            System.out.println("(For) Indíce: " + j);
        }
    }

}
